# Desempaquetado solo se realiza para listas, tuplas y conjuntos
datos = ["Stephen", "Curry", 36]

# Trata de que creamos variables separandolas por comas. Y en la posicion que esten esas variables se les asignan a la lista datos
# A esta accion se le llama desempaquetado
nombre, edad, apellido = datos

print(nombre) #? Stephen
print(apellido) #? Curry
print(edad) #? 36